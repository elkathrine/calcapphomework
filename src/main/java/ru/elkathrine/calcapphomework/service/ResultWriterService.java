package ru.elkathrine.calcapphomework.service;

public class ResultWriterService {
    public static void printing1(int num1, int num2, int result1) {
        System.out.println("Summary of " + num1 + " + " + num2 + " = " + result1);
    }

    public static void printing2(int num3, int num4, int result2) {
        System.out.println("Substraction of " + num3 + " - " + num4 + " = " + result2);
    }

    public static void printing3(int num5, int num6, int result3) {
        System.out.println("Multiplication of " + num5 + " * " + num6 + " = " + result3);
    }
}

