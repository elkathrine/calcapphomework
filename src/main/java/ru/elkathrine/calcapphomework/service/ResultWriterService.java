package ru.elkathrine.calcapphomework.service;

public class ResultWriterService {
    public static void printing1(int num1, int num2, int result) {
        System.out.println("Summary: " + num1 + " + " + num2 + " = " + result);
    }

    public static void printing2(int num1, int num2, int result) {
        System.out.println("Substraction: " + num1 + " - " + num2 + " = " + result);
    }

    public static void printing3(int num1, int num2, int result) {
        System.out.println("Multiplication: " + num1 + " * " + num2 + " = " + result);
    }

}

